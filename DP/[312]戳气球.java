        int[] val = new int[n + 2];
        val[0] = val[n + 1] = 1;
        for (int i = 1; i <= n; i++) {
            val[i] = nums[i - 1];
        }
        // åˆ©ç”¨å°åŒºé—´é‡Œå·²ç»ç®—å¥½çš„æ•°å­—æ¥ç®—æ›´å¤§çš„åŒºé—´
        // æœ€åˆçš„å°åŒºé—´æ˜¯æœ€åä¸‰ä¸ªæ•°[n - 1, n, n + 1]
